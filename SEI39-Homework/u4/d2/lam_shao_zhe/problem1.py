disney_characters = ["simba", "ariel", "pumba", "flounder", "nala", "ursula", "scar", "flotsam", "timon"]

for item in disney_characters:
    if "u" in item:
        print(item, "U are so Uniquely U!")
    elif "i" in item:
        print(item, "I bet you're Impressively Intelligent!")
    elif "o" in item:
        print(item, "O My! How Original!")
    else:
        print(item, "Ehh, a's and e's are so ordinary.")
